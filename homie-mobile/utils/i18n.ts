/**
 * Translation function. Takes a Portuguese string and returns
 * the current language version.
 */
export function t(ptString: string): string {
  const { lang } = useLanguageStore.getState();
  if (lang === 'pt') return ptString;
  return EN[ptString] || ptString;
}

/**
 * Hook version for use in components (triggers re-render on lang change).
 */
export function useT() {
  const lang = useLanguageStore((s) => s.lang);
  return (ptString: string): string => {
    if (lang === 'pt') return ptString;
    return EN[ptString] || ptString;
  };
}
